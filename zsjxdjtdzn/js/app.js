    // 加载章节内容
    function loadChapter(chapterIndex) {
        if (chapterIndex >= 0 && chapterIndex < novelData.length) {
            currentChapter = chapterIndex;
            const chapter = novelData[chapterIndex];
            
            // 更新章节信息
            chapterTitle.textContent = chapter.title;
            chapterIndicator.textContent = `第${chapterIndex + 1}章/共${novelData.length}章`;
            
            // 分页处理
            paginateContent(chapter.content);
            
            // 渲染第一页
            currentPage = 1;
            renderPage(currentPage);
            
            // 更新按钮状态
            updateNavigationButtons();
        }
    }
    
    // 内容分页函数
    function paginateContent(content) {
        const charsPerPage = 5000; // 每页大约字符数
        let currentPageChars = 0;
        let currentPageContent = [];
        pagesContent = [];
        
        content.forEach(paragraph => {
            const paraWithTags = `<p>${paragraph}</p>`;
            const paraLength = paraWithTags.length;
            
            if (currentPageChars + paraLength > charsPerPage && currentPageContent.length > 0) {
                pagesContent.push(currentPageContent.join(''));
                currentPageContent = [];
                currentPageChars = 0;
            }
            
            currentPageContent.push(paraWithTags);
            currentPageChars += paraLength;
        });
        
        if (currentPageContent.length > 0) {
            pagesContent.push(currentPageContent.join(''));
        }
        
        totalPages = pagesContent.length;
        totalPagesSpan.textContent = totalPages;
    }
    
    // 渲染页面内容
    function renderPage(pageNum) {
        const isPage1Active = pageNum % 2 === 1;
        const activePage = isPage1Active ? page1 : page2;
        const inactivePage = isPage1Active ? page2 : page1;
        
        // 设置活动页面
        activePage.innerHTML = pagesContent[pageNum - 1] || '<p>无内容</p>';
        activePage.classList.remove('prev', 'next');
        activePage.classList.add('active');
        
        // 设置非活动页面
        inactivePage.classList.remove('active');
        inactivePage.classList.add(isPage1Active ? 'next' : 'prev');
        
        // 更新页面指示器
        currentPageSpan.textContent = pageNum;
        
        // 更新按钮状态
        updateNavigationButtons();
    }
    
    // 更新导航按钮状态
    function updateNavigationButtons() {
        prevBtn.disabled = currentPage === 1;
        nextBtn.disabled = currentPage === totalPages;
        prevChapterBtn.disabled = currentChapter === 0;
        nextChapterBtn.disabled = currentChapter === novelData.length - 1;
    }
    
    // 事件监听
    prevBtn.addEventListener('click', function() {
        if (currentPage > 1) {
            currentPage--;
            renderPage(currentPage);
        }
    });
    
    nextBtn.addEventListener('click', function() {
        if (currentPage < totalPages) {
            currentPage++;
            renderPage(currentPage);
        }
    });
    
    prevChapterBtn.addEventListener('click', function() {
        if (currentChapter > 0) {
            loadChapter(currentChapter - 1);
        }
    });
    
    nextChapterBtn.addEventListener('click', function() {
        if (currentChapter < novelData.length - 1) {
            loadChapter(currentChapter + 1);
        }
    });
    
    // 键盘翻页支持
    document.addEventListener('keydown', function(e) {
        if (e.key === 'ArrowLeft') {
            if (!prevBtn.disabled) prevBtn.click();
            else if (!prevChapterBtn.disabled) prevChapterBtn.click();
        } else if (e.key === 'ArrowRight') {
            if (!nextBtn.disabled) nextBtn.click();
            else if (!nextChapterBtn.disabled) nextChapterBtn.click();
        }
    });
